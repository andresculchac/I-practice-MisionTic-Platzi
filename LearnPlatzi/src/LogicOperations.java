public class LogicOperations {
    public static void main(String[] args) {
        int a;
        int b;
        a = 8;
        b = 5;

        //assignment operator
        System.out.println("is a equal to b? --> " + (a == b));
        System.out.println("is a equal to b? --> " + (a != b));

        //relational operator
        System.out.println("is a equal to b? --> " + (a > b));
        System.out.println("is a equal to b? --> " + (a < b));
        System.out.println("is a equal to b? --> " + (a >= b));
        System.out.println("is a equal to b? --> " + (a <= b));

    }
}
